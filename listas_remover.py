alunos=['Carlos', ' Viktor', 'Lucas', 'Costa', 'Marcos', 'Carlos']
print(f"Lista de alunos: {alunos}")
# Método remove()
alunos.remove('Carlos')
print(f"Lista de alunos: {alunos}")
# Método pop()
alunos.pop(2)
print(f"Lista de alunos: {alunos}")
# Função del
del alunos[3]
print(f'Lista de alunos: {alunos}')
# Método clear()
alunos.clear()
print(f"Lista de alunos: {alunos}")